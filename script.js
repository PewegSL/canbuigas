function translatePage(lang) {
    // Actualizar todos los elementos con atributo data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Para elementos que pueden contener HTML
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA' || element.tagName === 'SELECT') {
                if (element.placeholder) {
                    element.placeholder = translations[lang][key];
                }
            } else if (element.tagName === 'OPTION') {
                // Mantener la bandera y actualizar solo el texto
                const flag = element.textContent.split(' ')[0];
                element.textContent = flag + ' ' + translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
}

